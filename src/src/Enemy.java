package src;

public class Enemy {
    String name;
    int hp;
    int dmg;

    Enemy(int hp, int dmg, String name) {
        this.hp = hp;
        this.dmg = dmg;
        this.name = name;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDmg() {
        return dmg;
    }

    public void setDmg(int dmg) {
        this.dmg = dmg;
    }
}
